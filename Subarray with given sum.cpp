class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        // Your code here
        long long initialSum = arr[0];
        for (int i=0, j = 0;i<n;){
            if(initialSum == s) return {i+1,j+1};
            else if(initialSum >s && i<j) initialSum -= arr[i++];
            else if(j<n-1) initialSum += arr[++j];
            else break;
        }
        return {-1};
    }
};

